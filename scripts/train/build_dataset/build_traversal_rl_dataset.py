@dataclass
class NodeCache:
    path: Tuple[int, ...]
    registry_idx: Optional[int]
    desc: str
    child_uids: List[int]
    doc_set: Set[str]
    doc_list: List[str]


def _tree_version_to_dag_version(tree_version: str) -> Optional[str]:
    prefix = "category-topdown-"
    tv = str(tree_version or "").strip()
    if tv.startswith(prefix):
        return tv[len(prefix):]
    return None


def _build_unrolled_tree_from_dag(
    *,
    subset: str,
    tree_version: str,
) -> Optional[SemanticNode]:
    dag_version = _tree_version_to_dag_version(tree_version)
    if not dag_version:
        return None

    dag_version_u = dag_version.replace("-", "_")
    dag_path = REPO_ROOT / "trees" / "BRIGHT" / subset / f"category_dag_topdown_{dag_version_u}.json"
    if not dag_path.exists():
        raise FileNotFoundError(f"DAG artifact not found for tree_version={tree_version}: {dag_path}")

    with dag_path.open("r", encoding="utf-8") as f:
        payload = json.load(f)

    nodes_raw = payload.get("nodes", [])
    edges_raw = payload.get("edges", [])
    if (not isinstance(nodes_raw, list)) or (not isinstance(edges_raw, list)):
        raise ValueError(f"Invalid DAG payload format: {dag_path}")

    row_by_id: Dict[str, Dict[str, Any]] = {}
    for row in nodes_raw:
        if not isinstance(row, dict):
            continue
        node_id = str(row.get("id", "")).strip()
        if not node_id:
            continue
        row_by_id[node_id] = row
    if not row_by_id:
        raise ValueError(f"No nodes found in DAG payload: {dag_path}")

    outgoing_ids: Dict[str, Set[str]] = defaultdict(set)
    for edge in edges_raw:
        if not isinstance(edge, dict):
            continue
        parent_id = str(edge.get("parent_id", "")).strip()
        child_id = str(edge.get("child_id", "")).strip()
        if (not parent_id) or (not child_id):
            continue
        if (parent_id not in row_by_id) or (child_id not in row_by_id):
            continue
        outgoing_ids[parent_id].add(child_id)

    root_id = ""
    for node_id, row in row_by_id.items():
        if str(row.get("kind", "")).strip().lower() == "root":
            root_id = node_id
            break
    if (not root_id) and ("L0|root" in row_by_id):
        root_id = "L0|root"
    if not root_id:
        root_id = sorted(row_by_id.keys())[0]

    reachable: Set[str] = set()
    queue: deque[str] = deque([root_id])
    while queue:
        cur = queue.popleft()
        if cur in reachable:
            continue
        reachable.add(cur)
        for nxt in sorted(outgoing_ids.get(cur, set())):
            if nxt not in reachable:
                queue.append(nxt)

    def _child_sort_key(child_id: str) -> Tuple[int, int, str]:
        row = row_by_id[child_id]
        level = int(row.get("level", 999))
        kind = str(row.get("kind", "")).strip().lower()
        is_leaf = 1 if kind == "leaf" else 0
        return (level, is_leaf, child_id)

    def _make_node(node_id: str, anc: Set[str]) -> SemanticNode:
        if node_id in anc:
            # Intent: defensive cycle guard; malformed DAG should not break unroll traversal generation.
            return SemanticNode(id=str(row_by_id[node_id].get("display_id", node_id)), desc="", child=[])
        row = row_by_id[node_id]
        display_id = row.get("display_id")
        semantic_id = display_id if (display_id is not None) else node_id
        node_desc = str(row.get("desc", "") or "")
        children: List[SemanticNode] = []
        next_anc = set(anc)
        next_anc.add(node_id)
        child_ids = [x for x in outgoing_ids.get(node_id, set()) if x in reachable]
        for child_id in sorted(child_ids, key=_child_sort_key):
            # Intent: unroll DAG multi-parent structure into path-distinct tree copies like run.py.
            children.append(_make_node(child_id, next_anc))
        return SemanticNode(id=semantic_id, desc=node_desc, child=children)

    root = _make_node(root_id, set())
    root.id = None
    return root


def load_tree_for_subset(subset: str, tree_version: str) -> SemanticNode:
    dag_root = _build_unrolled_tree_from_dag(subset=subset, tree_version=tree_version)
    if dag_root is not None:
        return dag_root
    tree_pkl = REPO_ROOT / "trees" / "BRIGHT" / subset / f"tree-{tree_version}.pkl"
    if not tree_pkl.exists():
        raise FileNotFoundError(f"Tree artifact not found for subset={subset}, tree_version={tree_version}: {tree_pkl}")
    return load_tree(tree_pkl)


def parse_tree_map_from_baseline_script(path: Path) -> Dict[str, str]:
    if not path.exists():
        raise FileNotFoundError(f"Baseline script not found: {path}")

    lines = path.read_text(encoding="utf-8").splitlines()
    in_runs_block = False
    mapping: Dict[str, str] = {}
    pattern = re.compile(r"--subset\s+([A-Za-z0-9_./-]+)\s+--tree_version\s+([A-Za-z0-9_./-]+)")

    for raw in lines:
        line = raw.strip()
        if line.startswith("RUNS=("):
            in_runs_block = True
            continue
        if in_runs_block and line.startswith(")"):
            break
        if not in_runs_block:
            continue
        if (not line) or line.startswith("#"):
            continue
        if '"' not in line and "'" not in line:
            continue

        if line.startswith('"') or line.startswith("'"):
            quote_char = line[0]
            try:
                run_str = ast.literal_eval(line.split("#", 1)[0].rstrip())
            except Exception:
                run_str = line.strip(quote_char).strip()
        else:
            run_str = line

        m = pattern.search(run_str)
        if m is None:
            continue
        subset = m.group(1).strip()
        tree_version = m.group(2).strip()
        if ("$" in subset) or ("{" in subset) or ("$" in tree_version) or ("{" in tree_version):
            continue
        mapping[subset] = tree_version

    return mapping


def compute_ndcg(sorted_preds: Sequence[str], gold: Sequence[str], k: int = 10) -> float:
    if not sorted_preds or not gold:
        return 0.0
    preds = list(sorted_preds)[:k]
    gold_set = set(gold)
    dcg = 0.0
    for i, pred in enumerate(preds):
        if pred in gold_set:
            dcg += 1.0 / np.log2(i + 2)
    ideal_k = min(k, len(gold_set))
    idcg = 0.0
    for i in range(ideal_k):
        idcg += 1.0 / np.log2(i + 2)
    if idcg == 0.0:
        return 0.0
    return float(dcg / idcg)


def get_node_doc_id(node_id, docs_df: pd.DataFrame) -> Optional[str]:
    try:
        if isinstance(node_id, str):
            if node_id.startswith("["):
                return node_id.split(" ", 1)[1]
            return node_id
        if isinstance(node_id, (int, np.int32, np.int64)):
            return str(docs_df.id.iloc[int(node_id)])
    except Exception:
        return None
    return None


def dedup_keep_order(items: Iterable[str]) -> List[str]:
    seen: Set[str] = set()
    ordered: List[str] = []
    for item in items:
        if item in seen:
            continue
        seen.add(item)
        ordered.append(item)
    return ordered


def normalize_text(text: str) -> str:
    return re.sub(r"\s+", " ", text).strip()


def parse_subsets(raw: str) -> List[str]:
    if raw.strip().lower() == "all":
        return list(DEFAULT_SUBSETS)
    return [x.strip() for x in raw.split(",") if x.strip()]


def parse_max_depth(raw: str) -> Optional[int]:
    value = str(raw).strip().lower()
    if value == "auto":
        return None
    depth = int(value)
    if depth <= 0:
        raise ValueError(f"--max_depth must be a positive integer or 'auto', got: {raw}")
    return depth


def iter_jsonl(path: Path, max_rows: int = -1):
    with path.open("r", encoding="utf-8") as f:
        for i, line in enumerate(f):
            if not line.strip():
                continue
            if max_rows > 0 and i >= max_rows:
                break
            yield i, json.loads(line)


def build_content_index(docs_df: pd.DataFrame) -> Dict[str, List[str]]:
    index: Dict[str, List[str]] = defaultdict(list)
    for _, row in docs_df.iterrows():
        doc_id = str(row["id"])
        content = str(row["content"])
        index[content].append(doc_id)
        index[normalize_text(content)].append(doc_id)
    return index


def map_texts_to_doc_ids(
    texts: Sequence[str],
    content_index: Dict[str, List[str]],
    valid_leaf_doc_ids: Set[str],
) -> Tuple[Set[str], int]:
    mapped: Set[str] = set()
    skipped_missing_leaf = 0
    for text in texts:
        key_exact = str(text)
        key_norm = normalize_text(key_exact)
        cand_ids = content_index.get(key_exact) or content_index.get(key_norm) or []
        picked = None
        for did in cand_ids:
            if did in valid_leaf_doc_ids:
                picked = did
                break
        if picked is None:
            skipped_missing_leaf += 1
            continue
        mapped.add(picked)
    return mapped, skipped_missing_leaf


def load_tree(root_pkl_path: Path) -> SemanticNode:
    class _CompatUnpickler(pkl.Unpickler):
        def find_class(self, module: str, name: str):
            # Intent: support legacy pickles where SemanticNode was serialized from __main__.
            if module == "__main__" and name == "SemanticNode":
                return SemanticNode
            return super().find_class(module, name)

    with root_pkl_path.open("rb") as f:
        try:
            tree_obj = pkl.load(f)
        except AttributeError as e:
            if "SemanticNode" not in str(e):
                raise
            f.seek(0)
            tree_obj = _CompatUnpickler(f).load()
    if isinstance(tree_obj, dict):
        return SemanticNode().load_dict(tree_obj)
    return tree_obj


def build_node_cache(root: SemanticNode, docs_df: pd.DataFrame) -> Tuple[int, Dict[int, NodeCache]]:
    cache: Dict[int, NodeCache] = {}

    def dfs(node: SemanticNode, path: Tuple[int, ...]) -> Tuple[Set[str], List[str]]:
        uid = id(node)
        child_uids: List[int] = []
        all_doc_set: Set[str] = set()
        all_doc_list: List[str] = []

        if len(node.child) == 0:
            did = get_node_doc_id(node.id, docs_df)
            if did is not None:
                all_doc_set.add(did)
                all_doc_list.append(did)
        else:
            for child_idx, child in enumerate(node.child):
                child_set, child_list = dfs(child, path + (child_idx,))
                child_uid = id(child)
                child_uids.append(child_uid)
                all_doc_set |= child_set
                all_doc_list.extend(child_list)
            all_doc_list = dedup_keep_order(all_doc_list)

        cache[uid] = NodeCache(
            path=path,
            registry_idx=getattr(node, "registry_idx", None),
            desc=str(getattr(node, "desc", "")),
            child_uids=child_uids,
            doc_set=all_doc_set,
            doc_list=all_doc_list,
        )
        return all_doc_set, all_doc_list

    dfs(root, tuple())
    return id(root), cache


def select_topk_candidates(
    candidate_uids: List[int],
    cache: Dict[int, NodeCache],
    pos_doc_ids: Set[str],
    neg_doc_ids: Set[str],
    beam_size: int,
    neg_penalty: float,
    empty_penalty: float,
) -> Tuple[List[dict], List[int], int]:
    scored: List[dict] = []
    for uid in candidate_uids:
        node_info = cache[uid]
        pos_overlap_count = len(node_info.doc_set & pos_doc_ids)
        has_pos = pos_overlap_count > 0
        has_neg = len(node_info.doc_set & neg_doc_ids) > 0
        if has_pos:
            reward = 1.0
        elif has_neg:
            reward = neg_penalty
        else:
            reward = empty_penalty
        scored.append(
            {
                "uid": uid,
                "path": list(node_info.path),
                "registry_idx": node_info.registry_idx,
                "desc": node_info.desc,
                "reward": float(reward),
                "has_pos": has_pos,
                "has_neg": has_neg,
                "pos_overlap_count": int(pos_overlap_count),
                "subtree_leaf_count": len(node_info.doc_list),
            }
        )

    # Intent: keep candidate list broad (including hard negatives) for supervision and analysis.
    scored.sort(key=lambda x: (-x["reward"], tuple(x["path"])))
    gold_candidates = [row for row in scored if bool(row["has_pos"])]
    # Intent: next frontier follows gold-containing branches only; no-gold frontier is handled by caller termination.
    selected = sorted(
        gold_candidates,
        key=lambda x: (-int(x["pos_overlap_count"]), int(x["subtree_leaf_count"]), tuple(x["path"])),
    )[:beam_size]
    selected_uids = [x["uid"] for x in selected]
    gold_candidate_count = len(gold_candidates)
    for row in scored:
        row.pop("uid", None)
    return scored, selected_uids, gold_candidate_count


def run_subset(
    subset: str,
    tree_version: str,
    reason_jsonl_path: Path,
    docs_parquet_path: Path,
    out_dir: Path,
    requested_max_depth: Optional[int],
    beam_size: int,
    neg_penalty: float,
    empty_penalty: float,
    max_samples: int,
) -> dict:
    docs_df = pd.read_parquet(docs_parquet_path, columns=["id", "content"])
    docs_df["id"] = docs_df["id"].astype(str)
    content_index = build_content_index(docs_df)

    tree_root = load_tree_for_subset(subset=subset, tree_version=tree_version)
    root_uid, node_cache = build_node_cache(tree_root, docs_df)
    valid_leaf_doc_ids = {did for info in node_cache.values() for did in info.doc_list if len(info.child_uids) == 0}
    tree_max_depth = max(len(info.path) for info in node_cache.values()) if node_cache else 0
    # Intent: keep traversal budget aligned with the actual tree depth used by the baseline unless user overrides it.
    effective_max_depth = tree_max_depth if requested_max_depth is None else requested_max_depth

    subset_out_dir = out_dir / subset
    subset_out_dir.mkdir(parents=True, exist_ok=True)
    branch_out_path = subset_out_dir / "branch_steps.jsonl"
    terminal_out_path = subset_out_dir / "terminal_rewards.jsonl"

    stats = {
        "subset": subset,
        "tree_version": tree_version,
        "requested_max_depth": ("auto" if requested_max_depth is None else requested_max_depth),
        "effective_max_depth": int(effective_max_depth),
        "tree_max_depth": int(tree_max_depth),
        "beam_size": beam_size,
        "neg_penalty": neg_penalty,
        "empty_penalty": empty_penalty,
        "num_docs": int(len(docs_df)),
        "num_nodes": int(len(node_cache)),
        "num_queries_total": 0,
        "num_queries_used": 0,
        "num_queries_skipped_no_pos": 0,
        "num_queries_skipped_no_candidates": 0,
        "num_queries_terminated_no_gold": 0,
        "num_branch_steps": 0,
        "num_steps_gold_pruned_mode": 0,
        "num_skipped_missing_leaf_posneg": 0,
        # Intent: final summary metric = arithmetic mean of per-query terminal nDCG@10 over queries that produced at least one step.
        "avg_terminal_ndcg_at_10": 0.0,
        "avg_depth_before_no_gold_termination": 0.0,
        "trajectory_mode": "gold_pruned_terminate",
    }

    terminal_ndcgs: List[float] = []
    no_gold_terminated_depths: List[int] = []

    with branch_out_path.open("w", encoding="utf-8") as branch_f, terminal_out_path.open("w", encoding="utf-8") as term_f:
        for query_idx, row in iter_jsonl(reason_jsonl_path, max_rows=max_samples):
            stats["num_queries_total"] += 1
            query = str(row.get("query", ""))
            pos_texts = row.get("pos") or []
            neg_texts = row.get("neg") or []

            pos_doc_ids, pos_skipped = map_texts_to_doc_ids(pos_texts, content_index, valid_leaf_doc_ids)
            neg_doc_ids, neg_skipped = map_texts_to_doc_ids(neg_texts, content_index, valid_leaf_doc_ids)
            stats["num_skipped_missing_leaf_posneg"] += int(pos_skipped + neg_skipped)

            if len(pos_doc_ids) == 0:
                stats["num_queries_skipped_no_pos"] += 1
                continue

            frontier_uids = [root_uid]
            executed_depth = 0
            query_has_step = False
            terminated_by_no_gold = False
            termination_reason = "max_depth_or_leaf"

            for depth in range(1, effective_max_depth + 1):
                candidate_uids: List[int] = []
                for fu in frontier_uids:
                    candidate_uids.extend(node_cache[fu].child_uids)
                candidate_uids = dedup_keep_order(candidate_uids)
                if len(candidate_uids) == 0:
                    break

                scored_candidates, selected_uids, gold_candidate_count = select_topk_candidates(
                    candidate_uids=candidate_uids,
                    cache=node_cache,
                    pos_doc_ids=pos_doc_ids,
                    neg_doc_ids=neg_doc_ids,
                    beam_size=beam_size,
                    neg_penalty=neg_penalty,
                    empty_penalty=empty_penalty,
                )
                if gold_candidate_count == 0:
                    terminated_by_no_gold = True
                    termination_reason = "no_gold_frontier"
                    stats["num_queries_terminated_no_gold"] += 1
                    no_gold_terminated_depths.append(int(executed_depth))
                    # Intent: stop episode when no gold-containing branch exists to avoid oracle recovery mismatch.
                    break
                if len(selected_uids) == 0:
                    break

                # Intent: store step-level supervision so branch policy can learn global-top2 decisions.
                branch_record = {
                    "subset": subset,
                    "query_idx": int(query_idx),
                    "depth": int(depth),
                    "query": query,
                    "frontier": [
                        {
                            "path": list(node_cache[uid].path),
                            "registry_idx": node_cache[uid].registry_idx,
                            "desc": node_cache[uid].desc,
                        }
                        for uid in frontier_uids
                    ],
                    "candidates": scored_candidates,
                    "gold_candidate_count": int(gold_candidate_count),
                    "selected_topk_paths": [list(node_cache[uid].path) for uid in selected_uids],
                    "selected_topk_rewards": [
                        next(x["reward"] for x in scored_candidates if x["path"] == list(node_cache[uid].path))
                        for uid in selected_uids
                    ],
                    "terminated_by_no_gold": False,
                    "trajectory_mode": "gold_pruned_terminate",
                    "pos_doc_count": len(pos_doc_ids),
                    "neg_doc_count": len(neg_doc_ids),
                }
                branch_f.write(json.dumps(branch_record, ensure_ascii=False) + "\n")

                frontier_uids = selected_uids
                executed_depth = depth
                stats["num_branch_steps"] += 1
                stats["num_steps_gold_pruned_mode"] += 1
                query_has_step = True

            if not query_has_step:
                stats["num_queries_skipped_no_candidates"] += 1
                continue

            terminal_doc_ids: List[str] = []
            for uid in frontier_uids:
                terminal_doc_ids.extend(node_cache[uid].doc_list)
            terminal_doc_ids = dedup_keep_order(terminal_doc_ids)

            # Intent: per-query terminal nDCG@10 is computed from final-frontier top10 docs against that query's mapped positive doc IDs.
            terminal_ndcg_at_10 = compute_ndcg(terminal_doc_ids[:10], list(pos_doc_ids), k=10)
            terminal_ndcgs.append(float(terminal_ndcg_at_10))

            # Intent: terminal reward is absolute nDCG@10 at final depth as agreed.
            terminal_record = {
                "subset": subset,
                "query_idx": int(query_idx),
                "query": query,
                "executed_depth": int(executed_depth),
                "max_depth": int(effective_max_depth),
                "terminated_by_no_gold": bool(terminated_by_no_gold),
                "termination_reason": termination_reason,
                "terminal_frontier_paths": [list(node_cache[uid].path) for uid in frontier_uids],
                "terminal_doc_ids_top10": terminal_doc_ids[:10],
                "gold_doc_ids": sorted(list(pos_doc_ids)),
                "terminal_ndcg_at_10": float(terminal_ndcg_at_10),
            }
            term_f.write(json.dumps(terminal_record, ensure_ascii=False) + "\n")
            stats["num_queries_used"] += 1

    # Intent: avg_terminal_ndcg_at_10 is a simple mean over per-query terminal nDCG@10 (no depth/step weighting).
    if terminal_ndcgs:
        stats["avg_terminal_ndcg_at_10"] = float(np.mean(terminal_ndcgs))
    if no_gold_terminated_depths:
        stats["avg_depth_before_no_gold_termination"] = float(np.mean(no_gold_terminated_depths))
    if stats["num_queries_used"] > 0:
        stats["avg_branch_steps_per_used_query"] = float(stats["num_branch_steps"] / stats["num_queries_used"])
    else:
        stats["avg_branch_steps_per_used_query"] = 0.0

    summary_path = subset_out_dir / "summary.json"
    with summary_path.open("w", encoding="utf-8") as f:
        json.dump(stats, f, ensure_ascii=False, indent=4)

    return stats


def main() -> None:
    parser = argparse.ArgumentParser(description="Build traversal-RL training data from reason-embed + BRIGHT trees.")
    parser.add_argument(
        "--subsets",
        type=str,
        default="biology,earth_science,economics,psychology,robotics,sustainable_living,stackoverflow,pony",
        help='Comma-separated subset names, or "all".',
    )
    parser.add_argument(
        "--max_depth",
        type=str,
        default="auto",
        help="Depth budget for traversal episodes. Use 'auto' to use the tree's actual max depth.",
    )
    parser.add_argument("--beam_size", type=int, default=2, help="Global top-k actions each step (fixed to 2 by default).")
    parser.add_argument("--neg_penalty", type=float, default=-0.1, help="Reward when no positive but hard-negative exists.")
    parser.add_argument("--empty_penalty", type=float, default=-1.0, help="Reward when neither pos nor neg exists in subtree.")
    parser.add_argument("--max_samples", type=int, default=-1, help="Optional cap per subset; -1 means all.")
    parser.add_argument(
        "--tree_map_source",
        type=str,
        choices=["baseline_script", "default"],
        default="baseline_script",
        help="How to resolve subset->tree_version mapping.",
    )
    parser.add_argument(
        "--baseline_run_script",
        type=str,
        default=str(REPO_ROOT / "src" / "bash" / "baselines" / "run_baseline1_tree_only.sh"),
        help="Baseline run script path used when --tree_map_source baseline_script.",
    )
    parser.add_argument(
        "--tree_version_override",
        type=str,
        default="",
        help="If set, force this tree_version for all subsets (e.g., category-topdown-algo4-v3).",
    )
    parser.add_argument(
        "--out_dir",
        type=str,
        default=str(REPO_ROOT / "data" / "train" / "tree_traversal_rl"),
        help="Output directory for generated training records.",
    )
    args = parser.parse_args()
    requested_max_depth = parse_max_depth(args.max_depth)

    subsets = parse_subsets(args.subsets)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    if args.tree_version_override:
        tree_version_map = {subset: args.tree_version_override for subset in subsets}
    elif args.tree_map_source == "baseline_script":
        baseline_path = Path(args.baseline_run_script)
        parsed_map = parse_tree_map_from_baseline_script(baseline_path)
        tree_version_map = dict(parsed_map)
    else:
        tree_version_map = dict(DEFAULT_TREE_VERSION_MAP)

    all_stats: List[dict] = []
    for subset in subsets:
        if subset not in tree_version_map:
            print(f"[skip] subset not found in resolved tree mapping: {subset}")
            continue

        tree_version = tree_version_map[subset]
        reason_jsonl = REPO_ROOT / "data" / "reason-embed-data-0928" / f"{subset}-formatted.jsonl"
        docs_parquet = REPO_ROOT / "data" / "BRIGHT" / "documents" / f"{subset}-00000-of-00001.parquet"
        if not reason_jsonl.exists():
            print(f"[skip] missing reason file: {reason_jsonl}")
            continue
        if not docs_parquet.exists():
            print(f"[skip] missing docs parquet: {docs_parquet}")
            continue
        dag_version = _tree_version_to_dag_version(tree_version)
        if dag_version is None:
            tree_pkl = REPO_ROOT / "trees" / "BRIGHT" / subset / f"tree-{tree_version}.pkl"
            if not tree_pkl.exists():
                print(f"[skip] missing tree pkl: {tree_pkl}")
                continue
        else:
            dag_version_u = dag_version.replace("-", "_")
            dag_json = REPO_ROOT / "trees" / "BRIGHT" / subset / f"category_dag_topdown_{dag_version_u}.json"
            if not dag_json.exists():
                print(f"[skip] missing DAG json: {dag_json}")
                continue

        print(f"[start] subset={subset} tree_version={tree_version}")
        stats = run_subset(
            subset=subset,
            tree_version=tree_version,
            reason_jsonl_path=reason_jsonl,
            docs_parquet_path=docs_parquet,
            out_dir=out_dir,
            requested_max_depth=requested_max_depth,
            beam_size=args.beam_size,
            neg_penalty=args.neg_penalty,
            empty_penalty=args.empty_penalty,
            max_samples=args.max_samples,
        )
        all_stats.append(stats)
        print(
            f"[done] subset={subset} used={stats['num_queries_used']}/{stats['num_queries_total']} "
            f"avg_ndcg@10={stats['avg_terminal_ndcg_at_10']:.4f}"
        )

    manifest = {
        "subsets": subsets,
        "requested_max_depth": ("auto" if requested_max_depth is None else requested_max_depth),
        "beam_size": args.beam_size,
        "neg_penalty": args.neg_penalty,
        "empty_penalty": args.empty_penalty,
        "max_samples": args.max_samples,
        "tree_map_source": args.tree_map_source,
        "baseline_run_script": args.baseline_run_script,
        "tree_version_override": args.tree_version_override,
        "resolved_tree_versions": {subset: tree_version_map.get(subset, "") for subset in subsets},
        "results": all_stats,
    }
    manifest_path = out_dir / "manifest.json"
    with manifest_path.open("w", encoding="utf-8") as f:
        json.dump(manifest, f, ensure_ascii=False, indent=4)
    print(f"[saved] {manifest_path}")


if __name__ == "__main__":
    main()
