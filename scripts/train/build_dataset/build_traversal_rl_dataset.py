#!/usr/bin/env python3
"""
Build traversal-RL supervision data from BRIGHT trees + reason-embed pos/neg sets.

Design decisions fixed from dev discussion:
- Action: global top-2 (beam size = 2)
- Trajectory: gold-pruned frontier; terminate episode when no gold-containing child exists
- Budget: max_depth
- Non-leaf reward:
    +1.0 if subtree has any positive doc
    -0.1 if subtree has no positive but has negative doc
    -1.0 if subtree has neither positive nor negative doc
- Terminal reward:
    nDCG@10 over terminal docs at final depth
- Missing leaf-mapped docs: skip from reward computation

How `branch_steps.jsonl` is generated (important):
- Output path: `{out_dir}/{subset}/branch_steps.jsonl`.
- The file is NOT generated by iterating over all tree nodes.
- It is also NOT limited to only strict gold-path nodes.
- Per query, the episode starts from root frontier and repeats by depth:
    1) Candidate set = union of children of current frontier nodes.
    2) Every candidate is scored and written into `candidates` (including non-gold branches).
    3) Next frontier is selected only from candidates whose subtree contains at least one positive doc (`has_pos=True`),
       with global top-k (default beam size 2).
             - “global top-k”라고만 쓰면 애매하고,
             - 실제 선택은 gold 후보 내에서 pos_overlap_count -> subtree_leaf_count -> path 정렬 기준이야 (scripts/train/build_dataset/
        build_traversal_rl_dataset.py:464).
    4) If there is no gold-containing candidate at a step, the episode terminates immediately (`no_gold_frontier`)
       and that terminating step is NOT written to `branch_steps.jsonl`.
- Therefore, one JSONL row = one executed branch decision step on a gold-pruned trajectory.
- Queries with no mapped positive doc are skipped entirely and produce no rows.
"""

