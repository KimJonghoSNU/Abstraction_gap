#!/usr/bin/env python3
"""
Build DPO preference dataset for traversal policy training.

Input:
- subset/*/branch_steps.jsonl generated by build_traversal_rl_dataset.py

Output:
- train_dpo.jsonl / eval_dpo.jsonl with columns expected by TRL DPOTrainer:
  {"prompt": str, "chosen": str, "rejected": str, ...metadata }
"""

from __future__ import annotations

import argparse
import itertools
import json
import random
import sys
from collections import defaultdict
from pathlib import Path
from typing import Dict, Iterable, List, Sequence, Tuple
from tqdm import tqdm
import numpy as np


EPS = 1e-6


def find_repo_root(start: Path) -> Path:
    for parent in [start, *start.parents]:
        if (parent / "src").exists() and (parent / "scripts").exists():
            return parent
    raise RuntimeError(f"Failed to locate repository root from {start}")


REPO_ROOT = find_repo_root(Path(__file__).resolve())
SRC_DIR = REPO_ROOT / "src"
TRAIN_DIR = REPO_ROOT / "scripts" / "train"
for target in [SRC_DIR, TRAIN_DIR]:
    if str(target) not in sys.path:
        sys.path.insert(0, str(target))

from prompt_builder import TraversalPromptBuilder  # noqa: E402


def parse_subsets(raw: str) -> List[str]:
    text = raw.strip()
    if text.lower() == "all":
        return []
    return [x.strip() for x in text.split(",") if x.strip()]


def discover_branch_files(data_root: Path, subsets: Sequence[str]) -> List[Tuple[str, Path]]:
    files: List[Tuple[str, Path]] = []
    if subsets:
        for subset in subsets:
            fp = data_root / subset / "branch_steps.jsonl"
            if fp.exists():
                files.append((subset, fp))
    else:
        for fp in sorted(data_root.glob("*/branch_steps.jsonl")):
            files.append((fp.parent.name, fp))
    return files


def collect_query_indices(path: Path, max_steps_per_subset: int) -> List[int]:
    query_ids = set()
    for step_idx, row in enumerate(iter_jsonl(path)):
        if max_steps_per_subset > 0 and step_idx >= max_steps_per_subset:
            break
        try:
            query_idx = int(row.get("query_idx", -1))
        except Exception:
            query_idx = -1
        if query_idx >= 0:
            query_ids.add(query_idx)
    return sorted(query_ids)


def iter_jsonl(path: Path):
    with path.open("r", encoding="utf-8") as f:
        for line in f:
            if not line.strip():
                continue
            yield json.loads(line)


def write_jsonl(path: Path, rows: Iterable[dict]) -> int:
    count = 0
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")
            count += 1
    return count


def reward_to_rel_score(reward: float) -> int:
    if reward >= 1.0 - EPS:
        return 95
    if reward <= -0.9:
        return 10
    return 40


def candidate_sort_key(cand: dict, idx: int) -> Tuple[float, Tuple[int, ...], int]:
    reward = float(cand.get("reward", 0.0))
    path = tuple(int(x) for x in cand.get("path", []))
    return (-reward, path, idx)


def sort_indices(indices: Iterable[int], candidates: Sequence[dict]) -> List[int]:
    return sorted(indices, key=lambda idx: candidate_sort_key(candidates[idx], idx))


def build_ranking(top2_set: Tuple[int, int], candidates: Sequence[dict]) -> List[int]:
    top2 = sort_indices(top2_set, candidates)
    remaining = [i for i in range(len(candidates)) if i not in set(top2)]
    remaining = sort_indices(remaining, candidates)
    return [*top2, *remaining]


def build_response_payload(ranking: Sequence[int], candidates: Sequence[dict]) -> str:
    relevance_scores = []
    for idx in ranking:
        reward = float(candidates[idx].get("reward", 0.0))
        relevance_scores.append([int(idx), int(reward_to_rel_score(reward))])

    payload = {
        "reasoning": "Selected ranking prioritizes branches that are more likely to contain answer-supporting evidence.",
        "ranking": [int(x) for x in ranking],
        "relevance_scores": relevance_scores,
    }
    return json.dumps(payload, ensure_ascii=False)


def make_pair_rows_for_step(
    *,
    subset: str,
    query_idx: int,
    depth: int,
    prompt: str,
    candidates: Sequence[dict],
    max_pairs_per_step_dpo: int,
    max_chosen_combos: int,
    rng: random.Random,
) -> List[dict]:
    num_candidates = len(candidates)
    if num_candidates < 2:
        return []

    pos_indices = [
        idx
        for idx, cand in enumerate(candidates)
        if float(cand.get("reward", 0.0)) >= 1.0 - EPS
    ]
    if len(pos_indices) == 0:
        return []

    pos_set = set(pos_indices)
    all_combos = list(itertools.combinations(range(num_candidates), 2))
    chosen_combos: List[Tuple[int, int]] = []

    if len(pos_indices) >= 2:
        # Intent: reduce tie-order bias by sampling multiple positive-positive top2 choices when many gold branches exist.
        chosen_combos = [tuple(sorted(x)) for x in itertools.combinations(pos_indices, 2)]
        rng.shuffle(chosen_combos)
        chosen_combos = chosen_combos[:max_chosen_combos]
    else:
        only_pos = pos_indices[0]
        non_pos = [x for x in range(num_candidates) if x != only_pos]
        non_pos = sort_indices(non_pos, candidates)
        if len(non_pos) == 0:
            return []
        # Intent: for |P|=1, enforce that chosen action always keeps the only gold branch in beam top2.
        chosen_combos = [tuple(sorted((only_pos, non_pos[0])))]

    def rejected_priority(combo: Tuple[int, int]) -> Tuple[int, int, float, Tuple[int, int]]:
        pos_hits = int(combo[0] in pos_set) + int(combo[1] in pos_set)
        avg_reward = (float(candidates[combo[0]].get("reward", 0.0)) + float(candidates[combo[1]].get("reward", 0.0))) / 2.0
        has_pos = 1 if pos_hits > 0 else 0
        return (has_pos, pos_hits, avg_reward, combo)

    rows: List[dict] = []
    for chosen_combo in chosen_combos:
        rejected_candidates = [x for x in all_combos if set(x) != set(chosen_combo)]
        rejected_candidates = sorted(rejected_candidates, key=rejected_priority)
        if len(rejected_candidates) == 0:
            continue

        per_chosen_quota = max(1, max_pairs_per_step_dpo // max(1, len(chosen_combos)))
        selected_rejected = rejected_candidates[:per_chosen_quota]
        for rejected_combo in selected_rejected:
            chosen_ranking = build_ranking(chosen_combo, candidates)
            rejected_ranking = build_ranking(rejected_combo, candidates)
            row = {
                "subset": subset,
                "query_idx": int(query_idx),
                "depth": int(depth),
                "prompt": prompt,
                "chosen": build_response_payload(chosen_ranking, candidates),
                "rejected": build_response_payload(rejected_ranking, candidates),
                "chosen_top2_set": [int(x) for x in sorted(chosen_combo)],
                "rejected_top2_set": [int(x) for x in sorted(rejected_combo)],
                "num_candidates": int(num_candidates),
                "num_positive_candidates": int(len(pos_indices)),
            }
            rows.append(row)

    rng.shuffle(rows)
    return rows[:max_pairs_per_step_dpo]


def split_by_query(rows: Sequence[dict], eval_ratio: float, seed: int) -> Tuple[List[dict], List[dict]]:
    grouped: Dict[str, List[dict]] = defaultdict(list)
    for row in rows:
        key = f"{row['subset']}::{row['query_idx']}"
        grouped[key].append(row)

    keys = list(grouped.keys())
    rng = random.Random(seed)
    rng.shuffle(keys)

    if len(keys) <= 1:
        return list(rows), []

    n_eval = int(round(len(keys) * eval_ratio))
    n_eval = min(max(n_eval, 1), len(keys) - 1)
    eval_keys = set(keys[:n_eval])

    train_rows: List[dict] = []
    eval_rows: List[dict] = []
    for key, bucket in grouped.items():
        if key in eval_keys:
            eval_rows.extend(bucket)
        else:
            train_rows.extend(bucket)
    return train_rows, eval_rows


def main() -> None:
    parser = argparse.ArgumentParser(description="Build DPO dataset from branch traversal supervision rows.")
    parser.add_argument("--data_root", type=str, required=True, help="Path containing subset/branch_steps.jsonl.")
    parser.add_argument("--out_dir", type=str, required=True, help="Output directory for DPO jsonl files.")
    parser.add_argument("--subsets", type=str, default="all", help='Comma-separated subset names or "all".')
    parser.add_argument("--eval_ratio", type=float, default=0.1)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--max_pairs_per_step_dpo", type=int, default=1)
    parser.add_argument("--max_chosen_combos", type=int, default=4)
    parser.add_argument("--max_steps_per_subset", type=int, default=-1)
    parser.add_argument(
        "--max_queries_per_subset",
        type=int,
        default=400,
        help="Maximum number of unique query_idx sampled per subset. Use -1 for no query-level cap.",
    )
    parser.add_argument("--max_prompt_proto_size", type=int, default=0)
    parser.add_argument("--max_desc_char_len", type=int, default=1200)
    parser.add_argument(
        "--prompt_template_file",
        type=str,
        default="",
        help=(
            "Optional custom prompt template file. "
            "Supported tokens: {{QUERY}}, {{CANDIDATES}}, {{RELEVANCE_DEFINITION}}, {{SUBSET}}, {{PROMPT_ID}}."
        ),
    )
    args = parser.parse_args()

    if args.max_pairs_per_step_dpo <= 0:
        raise ValueError("--max_pairs_per_step_dpo must be > 0 (use smaller values like 1~4 to reduce dataset size; -1 is not supported).")

    np.random.seed(args.seed)
    rng = random.Random(args.seed)
    prompt_builder = TraversalPromptBuilder(
        max_prompt_proto_size=args.max_prompt_proto_size,
        max_desc_char_len=args.max_desc_char_len,
        prompt_template_file=args.prompt_template_file,
    )

    data_root = Path(args.data_root)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    subsets = parse_subsets(args.subsets)
    branch_files = discover_branch_files(data_root, subsets)
    if len(branch_files) == 0:
        raise FileNotFoundError(f"No branch_steps.jsonl found under {data_root} for subsets={subsets or 'all'}")

    all_rows: List[dict] = []
    stats_by_subset: Dict[str, Dict[str, int]] = {}

    subset_iter = tqdm(branch_files, desc="subsets", unit="subset")
    for subset, branch_file in subset_iter:
        selected_query_idx_set = None
        num_queries_available = 0
        num_queries_selected = 0
        if args.max_queries_per_subset > 0:
            all_query_indices = collect_query_indices(branch_file, args.max_steps_per_subset)
            num_queries_available = len(all_query_indices)
            subset_rng = random.Random(f"{args.seed}:{subset}")
            if len(all_query_indices) > args.max_queries_per_subset:
                # Intent: cap per-subset data size at query granularity to avoid over-producing depth-expanded pairs.
                sampled_query_indices = subset_rng.sample(all_query_indices, args.max_queries_per_subset)
            else:
                sampled_query_indices = list(all_query_indices)
            selected_query_idx_set = set(int(x) for x in sampled_query_indices)
            num_queries_selected = len(selected_query_idx_set)

        stats = {
            "num_steps_seen": 0,
            "num_steps_used": 0,
            "num_pairs_generated": 0,
            "num_steps_skipped_no_candidates": 0,
            "num_steps_skipped_no_positive": 0,
            "num_steps_skipped_query_filter": 0,
            "num_queries_available": num_queries_available,
            "num_queries_selected": num_queries_selected,
        }

        step_iter = tqdm(iter_jsonl(branch_file), desc=f"{subset} steps", unit="step", leave=False)
        for step_idx, row in enumerate(step_iter):
            if args.max_steps_per_subset > 0 and step_idx >= args.max_steps_per_subset:
                break

            stats["num_steps_seen"] += 1
            query = str(row.get("query", ""))
            query_idx = int(row.get("query_idx", -1))
            if (selected_query_idx_set is not None) and (query_idx not in selected_query_idx_set):
                stats["num_steps_skipped_query_filter"] += 1
                continue

            candidates = row.get("candidates", []) or []
            if len(candidates) < 2:
                stats["num_steps_skipped_no_candidates"] += 1
                continue

            depth = int(row.get("depth", -1))
            candidate_descs = [str(x.get("desc", "")).strip() for x in candidates]
            prompt = prompt_builder.build(
                query=query,
                candidate_descs=candidate_descs,
                subset=subset,
            )

            pair_rows = make_pair_rows_for_step(
                subset=subset,
                query_idx=query_idx,
                depth=depth,
                prompt=prompt,
                candidates=candidates,
                max_pairs_per_step_dpo=args.max_pairs_per_step_dpo,
                max_chosen_combos=args.max_chosen_combos,
                rng=rng,
            )
            if len(pair_rows) == 0:
                stats["num_steps_skipped_no_positive"] += 1
                continue

            stats["num_steps_used"] += 1
            stats["num_pairs_generated"] += len(pair_rows)
            all_rows.extend(pair_rows)
            step_iter.set_postfix_str(f"pairs={stats['num_pairs_generated']}")

        stats_by_subset[subset] = stats
        subset_iter.set_postfix_str(f"{subset}:pairs={stats['num_pairs_generated']}")

    if len(all_rows) == 0:
        raise RuntimeError("No DPO pairs generated. Check reward labels and filtering configuration.")

    train_rows, eval_rows = split_by_query(all_rows, eval_ratio=args.eval_ratio, seed=args.seed)

    train_path = out_dir / "train_dpo.jsonl"
    eval_path = out_dir / "eval_dpo.jsonl"
    summary_path = out_dir / "summary.json"

    num_train = write_jsonl(train_path, train_rows)
    num_eval = write_jsonl(eval_path, eval_rows)

    summary = {
        "data_root": str(data_root),
        "subsets": subsets if subsets else "all",
        "num_subsets": len(branch_files),
        "num_pairs_total": len(all_rows),
        "num_pairs_train": num_train,
        "num_pairs_eval": num_eval,
        "eval_ratio": float(args.eval_ratio),
        "args": vars(args),
        "stats_by_subset": stats_by_subset,
    }
    summary_path.write_text(json.dumps(summary, ensure_ascii=False, indent=4), encoding="utf-8")

    print(f"[done] train={num_train} eval={num_eval} out_dir={out_dir}")


if __name__ == "__main__":
    main()
